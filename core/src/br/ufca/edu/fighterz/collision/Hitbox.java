package br.ufca.edu.fighterz.collision;

public final class Hitbox {
    final float x;
    final float y;
    final float width;
    final float height;

    public Hitbox(final float x, final float y, final float width, final float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
